create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session (
     id bigint generated by default as identity,
     name varchar(255) unique,
     start_at timestamp not null,
     end_at timestamp not null,
     price bigint not null,
     limit_count int default 0,
     progress_status ENUM('PREPARING', 'PROGRESSING', 'END'),
     recruitment_status ENUM('RECRUITING', 'NON_RECRUITMENT'),
     created_at timestamp not null,
     updated_at timestamp,
     user_id bigint not null,
     primary key (id)
);

create table session_image (
    id bigint generated by default as identity,
    image_size int not null,
    image_width double not null,
    image_height double not null,
    image_type ENUM('GIF', 'JPG', 'JPEG', 'PNG', 'SVG'),
    session_id bigint not null,
    primary key(id)
);

create table session_student (
    id bigint generated by default as identity,
    user_id bigint not null,
    registration_at timestamp not null,
    approval_status ENUM('WAITING', 'APPROVAL', 'CANCEL'),
    session_id bigint not null,
    primary key(id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);
